const provider = new ethers.JsonRpcProvider('HTTP://127.0.0.1:7545'); 
const contract = new ethers.Contract(contractAddress, contractABI, provider);

const verifyButton = document.getElementById('verifyButton');
const resultDiv = document.getElementById('result');

verifyButton.addEventListener('click', async () => {
    const tokenId = document.getElementById('tokenId').value;
    if (!tokenId) { alert('Vui lòng nhập ID văn bằng'); return; }
    resultDiv.innerHTML = 'Đang tìm kiếm...';
    try {
        const details = await contract.getCertificateDetails(tokenId);

        if (details.isRevoked) {
            resultDiv.innerHTML = `<h3>Kết quả xác thực: <span style="color:red;">ĐÃ BỊ THU HỒI</span></h3>
                                   <p>Văn bằng với ID <strong>${tokenId}</strong> đã bị vô hiệu hóa.</p>`;
            return; // Dừng lại không hiển thị thông tin khác
        }

        const graduationTimestamp = Number(details.graduationDate);
        const graduationDate = new Date(graduationTimestamp * 1000).toLocaleDateString('vi-VN');

        resultDiv.innerHTML = `
            <h3>Kết quả xác thực: <span style="color:green;">HỢP LỆ</span></h3>
            <p><strong>Tên sinh viên:</strong> ${details.studentName}</p>
            <p><strong>Ngành học:</strong> ${details.major}</p>
            <p><strong>Ngày cấp bằng:</strong> ${graduationDate}</p>
            <p><strong>Xếp loại:</strong> ${details.classification}</p>
        `;
    } catch (error) {
        resultDiv.innerHTML = `<h3>Kết quả xác thực: <span style="color:red;">KHÔNG TÌM THẤY</span></h3><p>Lỗi: Văn bằng với ID này không tồn tại hoặc có lỗi xảy ra.</p>`;
    }
});
